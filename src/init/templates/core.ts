// ─── Renderer ─────────────────────────────────────────────────────────────────

export function render(template: string, vars: Record<string, string>): string {
  return template.replace(/\{\{(\w+)\}\}/g, (_, key: string) => vars[key] ?? `{{${key}}}`);
}

// ─── AGENTS.md ────────────────────────────────────────────────────────────────

export function agentsMd(name: string, type: ProjectType, stack: string): string {
  return render(AGENTS_MD_TEMPLATE, { name, type, stack });
}

const AGENTS_MD_TEMPLATE = `# AGENTS — {{name}}

> lunatar multi-agent pipeline configuration.
> \`lunatar run "<intent>"\` — primary development workflow.

## Roles & models

| Role    | Default model        | Responsibility                               |
|---------|----------------------|----------------------------------------------|
| PO      | Llama 3.3 70B (Groq) | Clarify goal, extract requirements           |
| Planner | Gemini 2.5 Flash     | Architecture, tech breakdown, task list      |
| Dev     | Claude Sonnet 4.6    | Generate production-ready code               |
| QA      | Llama 3.3 70B (Groq) | Audit output, verdict PASS/FAIL, score 0–100 |

Override per run: press \`m\` in the TUI pipeline screen.

## Project

- **Name**: {{name}}
- **Type**: {{type}}
- **Stack**: {{stack}}

## Agent contracts

### PO
- Output JSON: \`{ clarifiedGoal, requirements[], constraints[], acceptanceCriteria[], complexity, assumptions[] }\`
- ≤ 12 requirements, each a clear testable statement — no implementation detail

### Planner
- Output JSON: \`{ architecture, techStack, tasks[], risks[] }\`
- Each task: \`{ id, description, files[], dependsOn[] }\`

### Dev
- Complete, runnable files — no \`// TODO\` placeholders
- Follow the stack: {{stack}}
- Must pass \`npm run lint\` and \`npm run typecheck\`
- TypeScript strict — no \`any\`, use \`unknown\` + type guards

### QA
- Output JSON: \`{ verdict: "PASS"|"FAIL", score: 0–100, issues[], suggestions[] }\`
- Issue: \`{ requirementId, description, severity: "low"|"medium"|"high" }\`

## Conventions

- \`import type\` for type-only imports
- Source in \`src/\`, tests in \`tests/\` (mirrors \`src/\`)
- Conventional Commits: \`feat:\` \`fix:\` \`refactor:\` \`test:\` \`chore:\`
- Vitest — ≥ 80% coverage on critical modules
`;

