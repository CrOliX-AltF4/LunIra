// ─── vitest.config.ts ─────────────────────────────────────────────────────────

export function vitestConfig(excludeEntry?: string): string {
  const excludeLine = excludeEntry
    ? `          exclude: ['${excludeEntry}', 'src/**/*.d.ts'],\n`
    : `          exclude: ['src/**/*.d.ts'],\n`;

  return `import { defineConfig } from 'vitest/config';

export default defineConfig({
  test: {
    globals: true,
    environment: 'node',
    include: ['tests/**/*.test.ts'],
    passWithNoTests: true,
    coverage: {
      provider: 'v8',
      reporter: ['text', 'json', 'html'],
      include: ['src/**/*.ts'],
${excludeLine}    },
  },
});
`;
}
